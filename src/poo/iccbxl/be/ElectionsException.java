package poo.iccbxl.be;

public class ElectionsException extends RuntimeException {

	public ElectionsException() {
		super();
	}

	public ElectionsException(String message, Throwable cause) {
		super(message, cause);
	}

	public ElectionsException(String arg0) {
		super(arg0);
	}

	public ElectionsException(Throwable arg0) {
		super(arg0);
	}
	
}
